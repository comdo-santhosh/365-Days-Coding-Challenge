public class Day11CountOccurenceInArray {
    public static void main(String[] args) {
        int num[] = {5,8,3,6,12,57};
        System.out.println(num.length);
        int count = 0;
        while (count < num.length) {
           count++;
        }
        
        System.out.println(count);
    }    
}
